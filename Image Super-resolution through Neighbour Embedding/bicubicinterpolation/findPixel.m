function newPoint = findPixel(w,x,y,z)
a = zeros(4,4, 'double');
%find a
a(1,1) = w(1);
a(1,2) = y(1);
a(1,3) = -3*w(1) + 3*w(3) -2*y(1) -y(3);
a(1,4) = 2*w(1) -2*w(3) + y(1) + y(3);

a(2,1) = x(1);
a(2,2) = z(1);
a(2,3) = -3*x(1) +3*x(3) -2*z(1) -z(3);
a(2,4) = 2*x(1) -2*x(3) + z(1) + z(3);

a(3,1) = -3*w(1) + 3*w(2) -2*x(1) -x(2);
a(3,2) = -3*y(1) +3*y(2) -2*z(1) - z(2);
a(3,3) = 9*(w(1) - w(2) - w(3) + w(4)) + 6*(x(1) - x(3) + y(1) - y(2)) + 4*z(1) + 3*(x(2) - x(4) + y(3) - y(4)) + 2*(z(2) + z(3)) + z(4);
a(3,4) = 6*(-w(1) + w(2) + w(3) - w(4)) - 4*x(1) -2*x(2) + 4*x(3) + 2*x(4) + 3*(-y(1) + y(2) -y(3) +y(4)) -2*z(1) -z(2) -2*z(3) -z(4); 

a(4,1) = 2*w(1) - 2*w(2) + x(1) + x(2);
a(4,2) = 2*y(1) - 2*y(2) + z(1) + z(2);
a(4,3) = 6*(-w(1) + w(2) + w(3) - w(4)) - 3*x(1) -3*x(2) + 3*x(3) + 3*x(4) - 4*y(1) + 4*y(2) -2*y(3) + 2*y(4) -2*z(1) -2*z(2) -z(3) -z(4); 
a(4,4) = 4*(w(1) - w(2) - w(3) + w(4)) + 2*(x(1) - x(3) + y(1) - y(2) + x(2) - x(4) + y(3) - y(4)) + z(1) + z(2) + z(3) + z(4);
m=0.5;
newPoint = a(1,1) + a(1,2)*(m) + a(1,3)*((m)^2) + a(1,4)*((m)^3) + a(2,1)*((m)) + a(2,2)*((m)^2) + a(2,3)*((m)^3) + a(2,4)*((m)^4) +  a(3,1)*((m)^2) + a(3,2)*((m)^3) + a(3,3)*((m)^4) + a(3,4)*((m)^5) + a(4,1)*((m)^3) + a(4,2)*((m)^4) + a(4,3)*((m)^5) + a(4,4)*((m)^6);